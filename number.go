// tipe data number :
// ada 2 jenis tipe data number, yaitu :
// integer dan floating point

// tipe data integer(1) :
// 1. int8 :
// 		nilai minimum : -128 dan nilai maksimum : 127
// 2. int16 :
// 		nilai minimum : -32768 dan nilai maksimum : 32767
// 3. int32 :
// 		nilai minimum : -2147483648 dan nilai maksimum : 2147483647
// 4. int64 :
// 		nilai minimum : -9223372036854775808 dan nilai maksimum : 9223372036854775807

// tipe data integer(2) :
// 1. uint8 :
// 		nilai minimum : 0 dan nilai maksimum : 255
// 2. uint16 :
// 		nilai minimum : 0 dan nilai maksimum : 65535
// 3. uint32 :
// 		nilai minimum : 0 dan nilai maksimum : 4294967295
// 4. int64 :
// 		nilai minimum : 0 dan nilai maksimum : 18446744073709551615

// tipe data floating point :
// 1. float32 :
// 		nilai minimum : 1.18x10(pangkat -38) dan nilai maksimum : 3.4x10(pangkat 38)
// 2. float64 :
// 		nilai minimum : 2.23x10(pangkat -308) dan nilai maksimum : 1.80x10(pangkat 308)
// 3. complex64 :
// 		nilai : complex numbers with float32 real and imaginary parts
// 4. complex128 :
// 		nilai : complex numbers with float64 real and imaginary parts

